     private boolean validarSeleccion(){
        boolean valor = false;
        int filaSeleccionada = this.tbGrupos.getSelectedRow();
        if (filaSeleccionada != -1) {
            valor= true;
        }else{
            JOptionPane.showMessageDialog(this, "Primero selccione un grupo.");
        }
        return valor;}
